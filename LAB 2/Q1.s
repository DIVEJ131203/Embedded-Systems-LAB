	AREA RESET,DATA,READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x40001000
	DCD Reset_Handler
	
	ALIGN
	
	AREA MYCODE,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R5,=RES
	LDR R6,=N
	LDR R0,=1
	LDR R1,=1
	LDR R2,=0
UP
	MLA R3,R0,R6,R2
	MOV R2,R3
	SUBS R6,#1
	BNE UP
	STR R2,[R5]
STOP
	B STOP
N   DCD 5
	AREA MYDATA,DATA,READWRITE
RES DCD 0x0
	END